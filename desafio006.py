### Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada.

num = int(input("Digite um número: "))
dobro = num * 2
triplo = num * 6
raiz = num ** (1 / 2)

print(f"O número digitado foi o: {num}\nO dobro: {dobro}\nTriplo: {triplo}\nRaiz Quadrada: {raiz}")
